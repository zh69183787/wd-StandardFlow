package com.wonders.framework.attachment.exception;

import com.wonders.framework.exception.WondersException;

public class AttachConfigException extends WondersException {

	public AttachConfigException() {
		super();
	}

	public AttachConfigException(String message, Throwable cause) {
		super(message, cause);
	}

	public AttachConfigException(String message) {
		super(message);
	}

	public AttachConfigException(Throwable cause) {
		super(cause);
	}
	
}
